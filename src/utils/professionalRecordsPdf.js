function drawDataTable(doc, columns, rows, cursor, options = {}) {
  const { width } = pageMetrics(doc);
  const x = options.x || REPORT.margin;
  const providedWidths = options.widths;
  const tableW = options.width || (providedWidths ? providedWidths.reduce((sum, item) => sum + item, 0) : width - REPORT.margin * 2);
  const widths = providedWidths || columns.map(() => tableW / columns.length);
  const headerH = 9;
  const rowH = options.rowHeight || 9;
  cursor = ensureSpace(doc, cursor, headerH + rowH + 6);

  const drawHeader = () => {
    doc.setFillColor(...REPORT.soft);
    doc.setDrawColor(...(options.borderColor || REPORT.border));
    doc.setLineWidth(options.lineWidth || 0.2);
    doc.rect(x, cursor.y, tableW, headerH, 'FD');
    let colX = x;
    doc.setFont('helvetica', 'bold');
    doc.setFontSize(options.fontSize || 8.5);
    doc.setTextColor(...REPORT.gunmetal);
    columns.forEach((column, index) => {
      doc.line(colX, cursor.y, colX, cursor.y + headerH);
      doc.text(column, colX + widths[index] / 2, cursor.y + 6, { align: 'center' });
      colX += widths[index];
    });
    doc.line(colX, cursor.y, colX, cursor.y + headerH);
    cursor.y += headerH;
  };

  drawHeader();
  rows.forEach((row) => {
    cursor = ensureSpace(doc, cursor, rowH + 8);
    let colX = x;
    doc.setDrawColor(...(options.borderColor || REPORT.border));
    doc.setLineWidth(options.lineWidth || 0.2);
    doc.rect(x, cursor.y, tableW, rowH);
    doc.setFont('helvetica', 'normal');
    doc.setFontSize(options.fontSize || 8.5);
    doc.setTextColor(...REPORT.text);
    row.forEach((cell, index) => {
      doc.line(colX, cursor.y, colX, cursor.y + rowH);
      const align = options.leftColumns?.includes(index) ? 'left' : 'center';
      const textX = align === 'left' ? colX + 3 : colX + widths[index] / 2;
      const text = doc.splitTextToSize(valueOrMissing(cell), widths[index] - 6)[0] || 'Not recorded';
      doc.text(text, textX, cursor.y + 6, { align });
      colX += widths[index];
    });
    doc.line(colX, cursor.y, colX, cursor.y + rowH);
    cursor.y += rowH;
  });

  return { ...cursor, y: cursor.y + 5 };
}

function buildClayReportData(record, reportData) {
  const { shotguns = {}, clubs = {}, locations = {}, clayData = {} } = reportData.lookups;
  const shotgun = shotguns[record.shotgun_id] || {};
  const scorecard = clayData.scorecards?.[record.id];
  const stands = clayData.stands?.[record.id] || [];
  const shots = clayData.shots?.[record.id] || {};
  const scoreData = buildClayScoreCardData(record, scorecard, stands, shots);
  return {
    clubName: resolveClayClubName(record, clubs, locations),
    duration: calculateDuration(record.checkin_time || record.start_time, record.checkout_time || record.end_time),
    shotgun,
    scoreData,
    photos: normalizePhotos(record.photos || []),
  };
}

function resolveTargetClubName(record, clubs = {}, locations = {}) {
  return record.club_name
    || record.range_name
    || record.location_name
    || record.place_name
    || record.venue_name
    || (record.club_id && clubs[record.club_id]?.name)
    || (record.location_id && (clubs[record.location_id]?.name || locations[record.location_id]?.name || locations[record.location_id]?.place_name))
    || 'Not Recorded';
}

function getTargetGroupPhoto(group = {}) {
  const photos = normalizePhotos(group.photos || []);
  return group.photo_url || group.target_photo_url || group.image_url || group.target_photo || photos[0] || null;
}

function buildTargetReportData(record, reportData) {
  const { rifles = {}, clubs = {}, locations = {}, targetGroups = [] } = reportData.lookups;
  const groups = targetGroups.filter((group) => group.session_id === record.id || group.session_record_id === record.id || group.target_session_id === record.id);
  return {
    clubName: resolveTargetClubName(record, clubs, locations),
    duration: calculateDuration(record.checkin_time || record.start_time, record.checkout_time || record.end_time),
    rifles,
    groups,
    photos: normalizePhotos(record.photos || []),
  };
}


import { jsPDF } from 'jspdf';
import { resolveClayClubName, buildClayScoreCardData, calculateDuration, normalizePhotos } from '@/lib/claySessionUtils';

const REPORT = {
  margin: 14,
  navy: [28, 48, 70],
  gunmetal: [48, 55, 64],
  copper: [196, 94, 18],
  text: [18, 24, 32],
  muted: [104, 112, 122],
  border: [210, 216, 224],
  soft: [245, 247, 250],
  white: [255, 255, 255],
};

function generateDocumentId() {
  const timestamp = Date.now().toString(36).toUpperCase();
  const random = Math.random().toString(36).substring(2, 8).toUpperCase();
  return `${timestamp}-${random}`;
}

function formatGeneratedDate(date = new Date()) {
  return `${date.toLocaleDateString('en-GB', { day: '2-digit', month: 'short', year: 'numeric' })} ${date.toLocaleTimeString('en-GB', { hour: '2-digit', minute: '2-digit' })}`;
}

function formatDate(value) {
  if (!value) return 'Not recorded';
  const date = new Date(value);
  if (Number.isNaN(date.getTime())) return value;
  return date.toLocaleDateString('en-GB', { day: '2-digit', month: 'short', year: 'numeric' });
}

function valueOrMissing(value) {
  if (value === null || value === undefined || value === '') return 'Not recorded';
  return String(value);
}

function getFullName(userInfo = {}) {
  return [userInfo.firstName, userInfo.middleName, userInfo.lastName].filter(Boolean).join(' ') || userInfo.full_name || userInfo.name || 'Not recorded';
}

function getAddress(userInfo = {}) {
  const parts = [userInfo.addressLine1, userInfo.addressLine2, userInfo.city, userInfo.postcode, userInfo.country].filter(Boolean);
  return parts.join(', ') || 'Not recorded';
}

async function getUserData(userInfo) {
  if (userInfo) return userInfo;
  try {
    const { base44 } = await import('@/api/base44Client');
    const authUser = await base44.auth.me();
    const fullUserData = await base44.entities.User.get(authUser.id);
    return {
      email: authUser.email,
      full_name: authUser.full_name,
      firstName: fullUserData.firstName || '',
      middleName: fullUserData.middleName || '',
      lastName: fullUserData.lastName || '',
      dateOfBirth: fullUserData.dateOfBirth || '',
      addressLine1: fullUserData.addressLine1 || '',
      addressLine2: fullUserData.addressLine2 || '',
      city: fullUserData.city || '',
      postcode: fullUserData.postcode || '',
      country: fullUserData.country || '',
    };
  } catch (error) {
    console.warn('Could not fetch user data for PDF:', error);
    return null;
  }
}

function categoryLabel(records = []) {
  const categories = new Set(records.map((record) => record.category || record.recordType));
  if (categories.size > 1) return 'Mixed Shooting Activities';
  const category = [...categories][0];
  if (category === 'clay_shooting' || category === 'clay') return 'Clay Shooting';
  if (category === 'target_shooting' || category === 'target') return 'Target Shooting';
  if (category === 'deer_management' || category === 'deer') return 'Deer Management';
  return 'Shooting Activity';
}

export function buildShootingReportData(records, userInfo, lookups = {}, documentId = generateDocumentId()) {
  const generatedAt = new Date();
  return {
    documentId,
    generatedAt,
    generatedAtLabel: formatGeneratedDate(generatedAt),
    category: categoryLabel(records),
    participant: {
      name: getFullName(userInfo || {}),
      email: valueOrMissing(userInfo?.email),
      dateOfBirth: userInfo?.dateOfBirth ? formatDate(userInfo.dateOfBirth) : 'Not recorded',
      address: getAddress(userInfo || {}),
    },
    records,
    lookups,
  };
}

function createDoc() {
  return new jsPDF({ orientation: 'portrait', unit: 'mm', format: 'a4' });
}

function pageMetrics(doc) {
  return { width: doc.internal.pageSize.getWidth(), height: doc.internal.pageSize.getHeight() };
}

function addReportFooter(doc, documentId, generatedAtLabel = null) {
  const { width, height } = pageMetrics(doc);
  const totalPages = doc.getNumberOfPages();
  for (let page = 1; page <= totalPages; page += 1) {
    doc.setPage(page);
    doc.setDrawColor(...REPORT.border);
    doc.line(REPORT.margin, height - 13, width - REPORT.margin, height - 13);
    doc.setFont('helvetica', 'normal');
    doc.setFontSize(7.5);
    doc.setTextColor(...REPORT.muted);
    doc.text(generatedAtLabel ? `Generated by Shooting Records | Generated: ${generatedAtLabel}` : 'Generated by Shooting Records', REPORT.margin, height - 8);
    doc.text(`Doc ID: ${documentId}`, width / 2, height - 8, { align: 'center' });
    doc.text(`Page ${page} of ${totalPages}`, width - REPORT.margin, height - 8, { align: 'right' });
  }
}

function ensureSpace(doc, cursor, needed) {
  const { height } = pageMetrics(doc);
  if (cursor.y + needed <= height - 18) return cursor;
  doc.addPage();
  return { ...cursor, y: REPORT.margin };
}

function drawHeader(doc, reportData, cursor) {
  const { width } = pageMetrics(doc);
  doc.setFillColor(...REPORT.navy);
  doc.roundedRect(REPORT.margin, cursor.y, width - REPORT.margin * 2, 28, 2, 2, 'F');
  doc.setTextColor(...REPORT.white);
  doc.setFont('helvetica', 'bold');
  doc.setFontSize(18);
  doc.text('SHOOTING ACTIVITY RECORD', REPORT.margin + 6, cursor.y + 11);
  doc.setFont('helvetica', 'normal');
  doc.setFontSize(9);
  doc.text('Official Regulatory Compliance Report', REPORT.margin + 6, cursor.y + 18);
  doc.setDrawColor(...REPORT.copper);
  doc.setLineWidth(1.2);
  doc.line(REPORT.margin + 6, cursor.y + 23, width - REPORT.margin - 6, cursor.y + 23);

  doc.setFontSize(8);
  doc.text(`Document ID: ${reportData.documentId}`, width - REPORT.margin - 6, cursor.y + 9, { align: 'right' });
  doc.text(`Generated: ${reportData.generatedAtLabel}`, width - REPORT.margin - 6, cursor.y + 15, { align: 'right' });
  doc.text(`Category: ${reportData.category}`, width - REPORT.margin - 6, cursor.y + 21, { align: 'right' });
  return { ...cursor, y: cursor.y + 36 };
}

function drawSectionTitle(doc, title, cursor) {
  const { width } = pageMetrics(doc);
  cursor = ensureSpace(doc, cursor, 16);
  doc.setFillColor(...REPORT.soft);
  doc.setDrawColor(...REPORT.border);
  doc.roundedRect(REPORT.margin, cursor.y, width - REPORT.margin * 2, 9, 1.5, 1.5, 'FD');
  doc.setFont('helvetica', 'bold');
  doc.setFontSize(10.5);
  doc.setTextColor(...REPORT.navy);
  doc.text(title, REPORT.margin + 4, cursor.y + 6.1);
  return { ...cursor, y: cursor.y + 15 };
}

function drawInfoCard(doc, title, rows, cursor, options = {}) {
  const { width } = pageMetrics(doc);
  const cardX = options.x || REPORT.margin;
  const cardW = options.width || width - REPORT.margin * 2;
  const colCount = options.columns || 2;
  const rowHeight = options.rowHeight || 12;
  const titleHeight = title ? 10 : 0;
  const contentPadding = title ? 4 : 2;
  const rowsPerLine = Math.ceil(rows.length / colCount);
  const cardH = titleHeight + contentPadding + rowsPerLine * rowHeight + 7;
  cursor = ensureSpace(doc, cursor, cardH + 4);

  doc.setFillColor(...REPORT.white);
  doc.setDrawColor(...REPORT.border);
  doc.roundedRect(cardX, cursor.y, cardW, cardH, 2, 2, 'FD');

  let y = cursor.y + 6;
  if (title) {
    doc.setFont('helvetica', 'bold');
    doc.setFontSize(9.5);
    doc.setTextColor(...REPORT.navy);
    doc.text(title, cardX + 4, y);
    y += 7;
    doc.setDrawColor(...REPORT.border);
    doc.line(cardX + 4, y - 2, cardX + cardW - 4, y - 2);
    y += contentPadding;
  }

  const colW = (cardW - 8) / colCount;
  rows.forEach((row, index) => {
    const col = index % colCount;
    const line = Math.floor(index / colCount);
    const x = cardX + 4 + col * colW;
    const rowY = y + line * rowHeight;
    doc.setFont('helvetica', 'bold');
    doc.setFontSize(8.5);
    doc.setTextColor(...REPORT.muted);
    doc.text(row.label, x, rowY + 1);
    doc.setFont('helvetica', 'normal');
    doc.setFontSize(9.5);
    doc.setTextColor(...REPORT.text);
    const valueLines = doc.splitTextToSize(valueOrMissing(row.value), colW - 6);
    doc.text(valueLines.slice(0, 2), x, rowY + 6);
  });

  return { ...cursor, y: cursor.y + cardH + 6 };
}


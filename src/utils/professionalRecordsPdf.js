function drawPhotosSection(doc, photos, cursor, title = 'PHOTOS') {
  if (title) cursor = drawSectionTitle(doc, title, cursor);
  if (!photos.length) {
    doc.setFont('helvetica', 'normal');
    doc.setFontSize(8.5);
    doc.setTextColor(...REPORT.text);
    doc.text('No Photos Recorded', REPORT.margin + 4, cursor.y);
    return { ...cursor, y: cursor.y + 10 };
  }

  const photoW = 40;
  const photoH = 30;
  const gap = 6;
  let x = REPORT.margin + 4;
  photos.forEach((photoUrl, index) => {
    cursor = ensureSpace(doc, cursor, photoH + 12);
    const { width } = pageMetrics(doc);
    if (x + photoW > width - REPORT.margin) {
      x = REPORT.margin + 4;
      cursor.y += photoH + 10;
      cursor = ensureSpace(doc, cursor, photoH + 12);
    }
    try {
      doc.addImage(photoUrl, 'JPEG', x, cursor.y, photoW, photoH);
      doc.link(x, cursor.y, photoW, photoH, { url: photoUrl });
    } catch (error) {
      doc.setDrawColor(...REPORT.border);
      doc.rect(x, cursor.y, photoW, photoH);
      doc.setFontSize(7);
      doc.setTextColor(...REPORT.muted);
      doc.text('Image unavailable', x + 5, cursor.y + 16);
    }
    doc.setFont('helvetica', 'normal');
    doc.setFontSize(7.5);
    doc.setTextColor(...REPORT.muted);
    doc.text(`Photo ${index + 1}`, x, cursor.y + photoH + 5);
    x += photoW + gap;
  });
  return { ...cursor, y: cursor.y + photoH + 13 };
}


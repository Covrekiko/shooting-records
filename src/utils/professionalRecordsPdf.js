function renderCategoryPage(doc, title, records, reportData, drawRecord) {
  doc.addPage();
  let cursor = drawCategoryTitle(doc, title);
  if (records.length === 0) {
    doc.setFont('helvetica', 'normal');
    doc.setFontSize(9.5);
    doc.setTextColor(...REPORT.text);
    doc.text('No records found for this category.', REPORT.margin + 2, cursor.y);
    return;
  }
  records.forEach((record) => {
    cursor = drawRecord(doc, record, reportData, cursor);
  });
}

function renderActivityLogRecords(doc, reportData, options = {}) {
  let cursor = drawActivityLogHeader(doc, reportData, { y: REPORT.margin });
  const selectedCategory = options.selectedCategory || options.category || 'all';
  const summaryRecords = getFilteredOverviewRecords(reportData.records, selectedCategory);

  cursor = drawInfoCard(doc, 'User Details', [
    { label: 'Name', value: reportData.participant.name },
    { label: 'Email', value: reportData.participant.email },
    { label: 'Date of Birth', value: reportData.participant.dateOfBirth },
    { label: 'Address', value: reportData.participant.address },
  ], cursor, { columns: 2, rowHeight: 16 });

  cursor = drawInfoCard(doc, 'Report Summary', buildReportSummaryRows(reportData, summaryRecords), cursor, { columns: 2, rowHeight: 15 });
  cursor = drawCategorySummaryTable(doc, summaryRecords, cursor);
  cursor = drawCoverNote(doc, cursor);

  const targetRecords = reportData.records.filter((record) => record.category === 'target_shooting' || record.recordType === 'target');
  const clayRecords = reportData.records.filter((record) => record.category === 'clay_shooting' || record.recordType === 'clay');
  const deerRecords = reportData.records.filter((record) => record.category === 'deer_management' || record.recordType === 'deer');

  if ((selectedCategory === 'all' && targetRecords.length) || selectedCategory === 'target_shooting' || selectedCategory === 'target') {
    renderCategoryPage(doc, 'Target Shooting', targetRecords, reportData, drawTargetOverviewRecord);
  }
  if ((selectedCategory === 'all' && clayRecords.length) || selectedCategory === 'clay_shooting' || selectedCategory === 'clay') {
    renderCategoryPage(doc, 'Clay Shooting', clayRecords, reportData, drawClayOverviewRecord);
  }
  if ((selectedCategory === 'all' && deerRecords.length) || selectedCategory === 'deer_management' || selectedCategory === 'deer') {
    renderCategoryPage(doc, 'Deer Management', deerRecords, reportData, drawDeerOverviewRecord);
  }
}

function renderRecords(doc, reportData, options = {}) {
  let cursor = { y: REPORT.margin };
  const isClayOnly = reportData.records.length > 0 && reportData.records.every((record) => record.category === 'clay_shooting' || record.recordType === 'clay');
  const isTargetOnly = reportData.records.length > 0 && reportData.records.every((record) => record.category === 'target_shooting' || record.recordType === 'target');
  const isDeerOnly = reportData.records.length > 0 && reportData.records.every((record) => record.category === 'deer_management' || record.recordType === 'deer');

  if (options.overview) {
    renderActivityLogRecords(doc, reportData, options);
    return;
  }

  if (!isClayOnly && !isTargetOnly) {
    renderActivityLogRecords(doc, reportData, options);
    return;
  }

  if (isClayOnly) {
    cursor = drawClayReportHeader(doc, reportData, cursor);
  } else if (isTargetOnly) {
    cursor = drawTargetReportHeader(doc, reportData, cursor);
  } else if (isDeerOnly) {
    cursor = drawClayPageTitle(doc, 'DEER MANAGEMENT SESSION REPORT', cursor, reportData);
  } else {
    cursor = drawHeader(doc, reportData, cursor);
    cursor = drawParticipantInfo(doc, reportData, cursor);
  }

  reportData.records.forEach((record, index) => {
    cursor = ensureSpace(doc, cursor, index === 0 ? 30 : 55);
    if (index > 0 && cursor.y > 220) {
      doc.addPage();
      cursor = { y: REPORT.margin };
      if (isClayOnly) cursor = drawClayReportHeader(doc, reportData, cursor);
      if (isTargetOnly) cursor = drawTargetReportHeader(doc, reportData, cursor);
      if (isDeerOnly) cursor = drawClayPageTitle(doc, 'DEER MANAGEMENT SESSION REPORT', cursor, reportData);
    }
    if (record.category === 'clay_shooting' || record.recordType === 'clay') {
      cursor = renderClaySession(doc, record, reportData, cursor, { includeTitle: !isClayOnly, separateScoreAndPhotos: isClayOnly });
    } else if (record.category === 'target_shooting' || record.recordType === 'target') {
      cursor = renderTargetSession(doc, record, reportData, cursor, { includeTitle: !isTargetOnly });
    } else if (record.category === 'deer_management' || record.recordType === 'deer') {
      cursor = renderDeerSession(doc, record, reportData, cursor);
    }
  });
}

export async function exportProfessionalRecordsToPdf(records, userInfo = null, fileName = 'shooting-records.pdf', rifles = {}, clubs = {}, shotguns = {}, locations = {}, targetGroups = [], clayData = {}, options = {}) {
  const userData = await getUserData(userInfo);
  const documentId = generateDocumentId();
  const reportData = buildShootingReportData(records, userData, { rifles, clubs, shotguns, locations, targetGroups, clayData }, documentId);
  const doc = createDoc();
  renderRecords(doc, reportData, options);
  addReportFooter(doc, documentId, options.overview ? reportData.generatedAtLabel : null);
  doc.save(fileName);
}

